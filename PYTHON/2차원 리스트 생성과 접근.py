

# [0, 0, 0] => 1차원 리스트 생성
a = [0]*3
print(a)

# 2차원 리스트 생성
a = [[0]*3 for _ in range(3)]
print(a)
# [[0, 0, 0], [0, 0, 0], [0, 0, 0]]

# 대입
a[0][1] = 1

a[1][1] = 2
print(a)
# [[0, 1, 0], [0, 2, 0], [0, 0, 0]]

# 1차원씩 출력
for x in a:
    print(x)
# [0, 1, 0]
# [0, 2, 0]
# [0, 0, 0]

# 숫자만 출력
for x in a:
    for y in x:
        print(y, end =' ')
    print()
#0 1 0 
#0 2 0 
#0 0 0
